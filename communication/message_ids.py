"""
CAN Message IDs and Commands
"""

# Status Messages
ENGINE_STATUS = 0x101
BODY_STATUS = 0x102
CLUSTER_STATUS = 0x103
GATEWAY_STATUS = 0x104

# Commands
BODY_COMMAND = 0x200

# Diagnostics
HEARTBEAT = 0x700

# Body Commands
LOCK_DOORS = 0x01
UNLOCK_DOORS = 0x02

HEADLIGHT_ON = 0x03
HEADLIGHT_OFF = 0x04

LEFT_INDICATOR = 0x05
RIGHT_INDICATOR = 0x06

HAZARD = 0x07